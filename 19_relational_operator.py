#example of relational expression 
a = int(input("Enter value for a"))
b = int(input("Enter value for b"))

result = a == b 
print(f"{result} = {a} == {b}")

result = a != b 
print(f"{result} = {a} != {b}")

result = a < b 
print(f"{result} = {a} < {b}")

result = a > b 
print(f"{result} = {a} > {b}")

result = a <= b 
print(f"{result} = {a} <= {b}")

result = a >= b 
print(f"{result} = {a} >= {b}")
